public class Birthday {
	
	private int ageYear;
	
	/**
	 * @return the ageYear
	 */
	public int getAgeYear() {
		return ageYear;
	}
	
	/**
	 * @param ageYear to set ageYear
	 */
	public void setAgeYear(int ageYear) {
		this.ageYear = ageYear;
	}
	/**
	 * @return the ageMonth
	 */
	public int getAgeMonth() {
		return ageMonth;
	}
	
	/**
	 * @param ageMonth to set ageMonth
	 */
	public void setAgeMonth(int ageMonth) {
		this.ageMonth = ageMonth;
	}
	
	/**
	 * @return the ageDays
	 */
	public int getAgeDays() {
		return ageDays;
	}
	/**
	 * @param ageDays to set ageDays
	 */
	public void setAgeDays(int ageDays) {
		this.ageDays = ageDays;
	}
	/**
	 * @return the nextDobMonths
	 */
	public int getNextDobMonths() {
		return nextDobMonths;
	}
	/**
	 * @param nextDobMonths to set nextDobMonths
	 */
	public void setNextDobMonths(int nextDobMonths) {
		this.nextDobMonths = nextDobMonths;
	}
	/**
	 * @return the nextDobDays
	 */
	public int getNextDobDays() {
		return nextDobDays;
	}
	
	/**
	 * @param nextDobDays to set nextDobMonths
	 */
	public void setNextDobDays(int nextDobDays) {
		this.nextDobDays = nextDobDays;
	}
	private int ageMonth;
	private int ageDays;
	private int nextDobMonths;
	private int nextDobDays;
	
	
	
}
